SYSTEM_PROMPT = (
    "Ты — аналитик маркетплейса Ozon. "
    "Классифицируй товары (смартфоны) по ценовому сегменту.\n\n"
    "Сегменты:\n"
    "- Эконом — бюджетные смартфоны, начальный сегмент\n"
    "- Стандарт — средний ценовой сегмент, массовый рынок\n"
    "- Премиум — флагманы и премиальные модели "
    "(Apple, Samsung Galaxy S/Ultra, и т.п.)\n\n"
    "Учитывай название, описание, бренд и характеристики — не только цену.\n"
    "Верни JSON с классификацией для каждого товара по его id."
)


def build_user_prompt(products: list[dict]) -> str:
    lines = ["Классифицируй следующие товары:\n"]
    for p in products:
        desc = p.get("description", "")[:200]
        lines.append(
            f"- id={p['id']}, название=\"{p['name']}\", "
            f"цена={p['price']} {p.get('currency', 'RUB')}"
            + (f", описание=\"{desc}\"" if desc else "")
        )
    return "\n".join(lines)
